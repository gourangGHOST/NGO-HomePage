/*==================== SMOOTH SCROLLING  ====================*/

function scrollToId(id) {
  const coords = getOffset(document.getElementById(id))
  console.log(coords)
  window.scrollTo({
    top: coords.top,
    behavior: 'smooth'
  })
}

function getOffset(el) {
  const rect = el.getBoundingClientRect();
  return {
    left: rect.left + window.scrollX,
    top: rect.top + window.scrollY
  };
}
